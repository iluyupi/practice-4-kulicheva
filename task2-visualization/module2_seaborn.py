# =============================================================================
# ГРАФИК 3. Динамика рентабельности — lineplot
# =============================================================================
# Зачем этот график: отображает, как менялась отдача от активов, капитала
# и продаж за три года. Три кривые на одном поле позволяют сравнить
# темпы роста разных показателей рентабельности.

# Переводим в проценты для наглядности
df_prof = df_ratios[["year", "roa", "roe", "ros"]].copy()
df_prof["roa"] = (df_prof["roa"] * 100).round(1)
df_prof["roe"] = (df_prof["roe"] * 100).round(1)
df_prof["ros"] = (df_prof["ros"] * 100).round(1)

# Приводим к «длинному» формату для seaborn
df_long = df_prof.melt(id_vars="year", var_name="indicator", value_name="value")

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: русскоязычные названия показателей в легенде          │
# └─────────────────────────────────────────────────────────────────────────┘
label_map = {"roa": "ROA — рент. активов", "roe": "ROE — рент. капитала",
             "ros": "ROS — рент. продаж"}
df_long["indicator"] = df_long["indicator"].map(label_map)

palette = {"ROA — рент. активов": "#2E5090",
           "ROE — рент. капитала": "#E8844A",
           "ROS — рент. продаж": "#A8D08D"}

fig3, ax3 = plt.subplots(figsize=(10, 5))

sns.lineplot(
    data=df_long, x="year", y="value",
    hue="indicator", palette=palette,
    marker="o", markersize=9, linewidth=2.5,
    ax=ax3,
)

# Подписи значений на каждой точке
for _, row in df_long.iterrows():
    ax3.annotate(
        f"{row['value']}%",
        xy=(row["year"], row["value"]),
        xytext=(0, 10), textcoords="offset points",
        ha="center", fontsize=9,
    )

ax3.set_xticks(df_ratios["year"].tolist())
ax3.set_xticklabels([str(y) for y in df_ratios["year"].tolist()], fontsize=11)
ax3.grid(axis="y", linestyle="--", alpha=0.4)
ax3.set_ylabel("Рентабельность, %", fontsize=11)
ax3.set_xlabel("Год", fontsize=11)
ax3.legend(title="", fontsize=10, loc="upper left")

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: заголовок                                             │
# └─────────────────────────────────────────────────────────────────────────┘
ax3.set_title("Динамика показателей рентабельности, 2022–2024",
              fontsize=13, fontweight="bold", pad=12)

plt.tight_layout()
plt.savefig("charts/m2_profit_dynamics.png", dpi=150, bbox_inches="tight")
plt.show()
print("График 3 сохранён: charts/m2_profit_dynamics.png")

