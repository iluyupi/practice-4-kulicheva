# =============================================================================
# ГРАФИК 2. Интерактивный столбчатый — коэффициенты за три года
# =============================================================================
# Зачем этот график: позволяет сравнивать несколько коэффициентов
# одновременно по годам. Кликом по году в легенде можно скрыть его данные
# и сосредоточиться на нужном периоде.

years_rat = df_ratios["year"].astype(str).tolist()

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: выберите, какие коэффициенты показывать.              │
# │  Можно убрать строку или добавить новый коэффициент из листа ratios.   │
# └─────────────────────────────────────────────────────────────────────────┘
indicators = {
    "Текущая ликвидность": ("current_ratio", "#2E5090"),
    "Быстрая ликвидность":  ("quick_ratio",   "#5B9BD5"),
    "Коэф. автономии":      ("autonomy",       "#A8D08D"),
}

fig2 = go.Figure()

for label, (col, color) in indicators.items():
    fig2.add_trace(go.Bar(
        name=label,
        x=years_rat,
        y=df_ratios[col],
        marker_color=color,
        text=[f"{v:.2f}" for v in df_ratios[col]],
        textposition="outside",
        hovertemplate=f"<b>{label}</b><br>Год: %{{x}}<br>Значение: %{{y:.2f}}<extra></extra>",
    ))

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: заголовок                                             │
# └─────────────────────────────────────────────────────────────────────────┘
fig2.update_layout(
    title=dict(
        text="Финансовые коэффициенты по годам",
        font=dict(size=16),
        x=0.5,
    ),
    xaxis_title="Год",
    yaxis_title="Значение",
    barmode="group",          # столбцы рядом, а не накоплением
    legend=dict(orientation="h", y=-0.25),
    plot_bgcolor="white",
    yaxis=dict(gridcolor="#EEEEEE"),
    font=dict(family="Arial", size=13),
    width=900, height=500,
)

fig2.write_html("charts/m3_ratios_interactive.html")
fig2.show()
print("График 2 сохранён: charts/m3_ratios_interactive.html")

