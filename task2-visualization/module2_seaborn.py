import os
import pandas as pd
import numpy as np
import matplotlib
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches
import seaborn as sns

# Поддержка кириллицы
matplotlib.rcParams["font.sans-serif"] = ["Arial", "Liberation Sans", "DejaVu Sans"]
matplotlib.rcParams["axes.unicode_minus"] = False

os.makedirs("charts", exist_ok=True)


# =============================================================================
# БЛОК ДАННЫХ
# =============================================================================

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: укажите путь к вашему файлу данных                    │
# └─────────────────────────────────────────────────────────────────────────┘
DATA_FILE = "data.xlsx"

df_ratios = pd.read_excel(DATA_FILE, sheet_name="ratios")

print("Данные коэффициентов загружены:")
print(df_ratios)


# =============================================================================
# ГРАФИК 1. Тепловая карта финансовых коэффициентов — heatmap
# =============================================================================
# Зачем этот график: позволяет одним взглядом охватить все показатели за
# три года. Тёмный/светлый цвет клетки сигнализирует о высоком/низком
# значении. Удобен для обнаружения трендов и аномалий.

# Формируем матрицу: строки — годы, столбцы — коэффициенты
heatmap_data = df_ratios.set_index("year")

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: русскоязычные названия столбцов для отображения       │
# └─────────────────────────────────────────────────────────────────────────┘
col_labels = {
    "current_ratio": "Текущая\nликвидность",
    "quick_ratio":   "Быстрая\nликвидность",
    "autonomy":      "Коэф.\nавтономии",
    "roa":           "ROA",
    "roe":           "ROE",
    "ros":           "ROS",
}
heatmap_data = heatmap_data.rename(columns=col_labels)

fig1, ax1 = plt.subplots(figsize=(11, 4))

sns.heatmap(
    heatmap_data,
    annot=True,          # показывать значения в ячейках
    fmt=".2f",           # формат: два знака после запятой
    cmap="YlGnBu",       # цветовая палитра (жёлто-зелёно-синяя)
    linewidths=0.5,
    linecolor="white",
    ax=ax1,
    annot_kws={"size": 11, "weight": "bold"},
)

ax1.set_yticklabels(
    [str(y) for y in df_ratios["year"].tolist()],
    rotation=0, fontsize=11,
)
ax1.set_xticklabels(ax1.get_xticklabels(), rotation=0, fontsize=10)

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: заголовок — укажите название предприятия              │
# └─────────────────────────────────────────────────────────────────────────┘
ax1.set_title("Финансовые коэффициенты предприятия, 2022–2024",
              fontsize=13, fontweight="bold", pad=12)
ax1.set_ylabel("Год", fontsize=11)
ax1.set_xlabel("")

plt.tight_layout()
plt.savefig("charts/m2_ratios_heatmap.png", dpi=150, bbox_inches="tight")
plt.show()
print("График 1 сохранён: charts/m2_ratios_heatmap.png")

