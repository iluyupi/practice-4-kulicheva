import os
import pandas as pd
import plotly.graph_objects as go
from plotly.subplots import make_subplots

os.makedirs("charts", exist_ok=True)


# =============================================================================
# БЛОК ДАННЫХ
# =============================================================================

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: укажите путь к вашему файлу данных                    │
# └─────────────────────────────────────────────────────────────────────────┘
DATA_FILE = "data.xlsx"

df_results = pd.read_excel(DATA_FILE, sheet_name="results")
df_ratios  = pd.read_excel(DATA_FILE, sheet_name="ratios")

print("Данные загружены.")

years_str = df_results["year"].astype(str).tolist()


# =============================================================================
# ГРАФИК 1. Интерактивная динамика финансовых результатов
# =============================================================================
# Зачем этот график: аналог графика из Модуля 1, но интерактивный.
# При наведении курсора появляется подсказка с точными значениями.
# Можно масштабировать отдельные периоды, скачать как PNG.
#
# Попробуйте: кликните на название в легенде, чтобы скрыть/показать линию.

fig1 = go.Figure()

# Линия выручки
fig1.add_trace(go.Scatter(
    x=years_str,
    y=df_results["revenue"],
    mode="lines+markers+text",
    name="Выручка",
    line=dict(color="#2E5090", width=3),
    marker=dict(size=10),
    text=[f"{v:,}" for v in df_results["revenue"]],
    textposition="top center",
    # Подсказка при наведении курсора:
    hovertemplate="<b>%{x}</b><br>Выручка: %{y:,.0f} тыс. руб.<extra></extra>",
))

# Линия чистой прибыли
fig1.add_trace(go.Scatter(
    x=years_str,
    y=df_results["net_profit"],
    mode="lines+markers+text",
    name="Чистая прибыль",
    line=dict(color="#E8844A", width=3, dash="dash"),
    marker=dict(size=10, symbol="square"),
    text=[f"{v:,}" for v in df_results["net_profit"]],
    textposition="top center",
    hovertemplate="<b>%{x}</b><br>Чистая прибыль: %{y:,.0f} тыс. руб.<extra></extra>",
))

# Линия валовой прибыли
fig1.add_trace(go.Scatter(
    x=years_str,
    y=df_results["gross_profit"],
    mode="lines+markers",
    name="Валовая прибыль",
    line=dict(color="#A8D08D", width=2, dash="dot"),
    marker=dict(size=8),
    hovertemplate="<b>%{x}</b><br>Валовая прибыль: %{y:,.0f} тыс. руб.<extra></extra>",
))

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: заголовок и подписи осей                              │
# └─────────────────────────────────────────────────────────────────────────┘
fig1.update_layout(
    title=dict(
        text="Динамика финансовых результатов, тыс. руб.",
        font=dict(size=16),
        x=0.5,
    ),
    xaxis_title="Год",
    yaxis_title="Тыс. руб.",
    legend=dict(orientation="h", y=-0.2),
    hovermode="x unified",   # подсказка сразу для всех линий в точке X
    plot_bgcolor="white",
    yaxis=dict(gridcolor="#EEEEEE"),
    font=dict(family="Arial", size=13),
    width=900, height=500,
)

# Сохранить как HTML
fig1.write_html("charts/m3_revenue_interactive.html")
# Открыть в браузере сразу после запуска скрипта:
fig1.show()
print("График 1 сохранён: charts/m3_revenue_interactive.html")

