# =============================================================================
# ГРАФИК 2. Коэффициенты ликвидности vs норматив — barplot
# =============================================================================
# Зачем этот график: сравнивает фактические значения показателей ликвидности
# с нормативными. Красная пунктирная линия обозначает минимально допустимый
# уровень. Столбец ниже линии — сигнал о проблеме.

# Берём данные последнего года
last_year = df_ratios[df_ratios["year"] == df_ratios["year"].max()].iloc[0]

liquidity_data = pd.DataFrame({
    "Коэффициент": [
        "Текущая\nликвидность",
        "Быстрая\nликвидность",
        "Абсолютная\nликвидность",
    ],
    "Факт": [
        last_year["current_ratio"],
        last_year["quick_ratio"],
        # Абсолютная ликвидность в данных не хранится — укажите вручную:
        # ┌───────────────────────────────────────────────────────────┐
        # │  ИЗМЕНИТЕ ЗДЕСЬ: фактическое значение абсолютной         │
        # │  ликвидности из отчёта audit-it.ru                       │
        # └───────────────────────────────────────────────────────────┘
        0.18,
    ],
})

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: нормативные значения (стандартные значения уже        │
# │  проставлены; уточните по методическому источнику, если нужно)         │
# └─────────────────────────────────────────────────────────────────────────┘
norms = {
    "Текущая\nликвидность":    2.0,
    "Быстрая\nликвидность":    1.0,
    "Абсолютная\nликвидность": 0.2,
}

# Цвет столбца: зелёный — выше норматива, красный — ниже
colors = [
    "#A8D08D" if row["Факт"] >= norms[row["Коэффициент"]] else "#FF7C7C"
    for _, row in liquidity_data.iterrows()
]

fig2, ax2 = plt.subplots(figsize=(9, 5))

bars = ax2.bar(
    liquidity_data["Коэффициент"],
    liquidity_data["Факт"],
    color=colors,
    width=0.45,
    zorder=3,
)

# Подписи значений над столбцами
for bar in bars:
    h = bar.get_height()
    ax2.text(bar.get_x() + bar.get_width() / 2, h + 0.03,
             f"{h:.2f}", ha="center", va="bottom", fontsize=11, fontweight="bold")

# Линии нормативов
for i, (label, norm_val) in enumerate(norms.items()):
    ax2.hlines(norm_val, i - 0.3, i + 0.3,
               colors="#CC2222", linewidths=2, linestyles="--", zorder=4)
    ax2.text(i + 0.32, norm_val, f"норма {norm_val}",
             va="center", fontsize=9, color="#CC2222")

ax2.grid(axis="y", linestyle="--", alpha=0.4, zorder=0)

# Легенда
patch_ok  = mpatches.Patch(color="#A8D08D", label="Выше норматива")
patch_bad = mpatches.Patch(color="#FF7C7C", label="Ниже норматива")
ax2.legend(handles=[patch_ok, patch_bad], fontsize=10, loc="upper right")

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: заголовок — укажите год                               │
# └─────────────────────────────────────────────────────────────────────────┘
ax2.set_title("Коэффициенты ликвидности: факт vs норматив (2024)",
              fontsize=13, fontweight="bold", pad=12)
ax2.set_ylabel("Значение коэффициента", fontsize=11)
ax2.set_ylim(0, max(liquidity_data["Факт"].max(), 2.2) * 1.2)

plt.tight_layout()
plt.savefig("charts/m2_ratios_vs_norm.png", dpi=150, bbox_inches="tight")
plt.show()
print("График 2 сохранён: charts/m2_ratios_vs_norm.png")

