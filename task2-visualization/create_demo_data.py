import pandas as pd
import os

# --- Путь к файлу ---------------------------------------------------------
# Файл будет создан в той же папке, что и этот скрипт.
OUTPUT_FILE = "data.xlsx"

# =============================================================================
# ЛИСТ 1: results — Отчёт о финансовых результатах
# Единица измерения: тысячи рублей
# =============================================================================

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: замените числа на данные вашего предприятия           │
# │  Источник: аналитическая записка из Задания 1 (analysis.docx)          │
# └─────────────────────────────────────────────────────────────────────────┘

df_results = pd.DataFrame({
    "year":         [2022,   2023,   2024],     # годы отчётных периодов
    "revenue":      [145000, 162000, 178000],   # выручка
    "cost":         [98000,  108000, 115000],   # себестоимость
    "gross_profit": [47000,  54000,  63000],    # валовая прибыль
    "net_profit":   [12000,  15500,  21000],    # чистая прибыль
})

# =============================================================================
# ЛИСТ 2: ratios — Финансовые коэффициенты
# =============================================================================

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: замените значения коэффициентов                       │
# │  Источник: отчёт audit-it.ru из Задания 1                              │
# └─────────────────────────────────────────────────────────────────────────┘

df_ratios = pd.DataFrame({
    "year":          [2022, 2023, 2024],  # годы
    "current_ratio": [1.4,  1.6,  1.9],  # коэффициент текущей ликвидности
    "quick_ratio":   [0.9,  1.1,  1.3],  # коэффициент быстрой ликвидности
    "autonomy":      [0.42, 0.47, 0.51], # коэффициент автономии
    "roa":           [0.06, 0.09, 0.12], # рентабельность активов (ROA)
    "roe":           [0.14, 0.18, 0.22], # рентабельность собственного капитала (ROE)
    "ros":           [0.08, 0.10, 0.12], # рентабельность продаж (ROS)
})

# =============================================================================
# ЛИСТ 3: balance — Структура баланса (последний отчётный год)
# Единица измерения: тысячи рублей
# =============================================================================

# ┌─────────────────────────────────────────────────────────────────────────┐
# │  ИЗМЕНИТЕ ЗДЕСЬ: замените значения статей баланса                      │
# └─────────────────────────────────────────────────────────────────────────┘

df_balance = pd.DataFrame({
    "category": [
        "Внеоборотные активы",
        "Оборотные активы",
        "Собственный капитал",
        "Долгосрочные обязательства",
        "Краткосрочные обязательства",
    ],
    "value": [89000, 61000, 76000, 34000, 40000],
})

# =============================================================================
# ЗАПИСЬ В ФАЙЛ — этот блок менять не нужно
# =============================================================================

with pd.ExcelWriter(OUTPUT_FILE, engine="openpyxl") as writer:
    df_results.to_excel(writer, sheet_name="results", index=False)
    df_ratios.to_excel(writer, sheet_name="ratios",  index=False)
    df_balance.to_excel(writer, sheet_name="balance", index=False)

print(f"Файл '{OUTPUT_FILE}' создан успешно.")
print(f"Листы: results ({len(df_results)} строк), "
      f"ratios ({len(df_ratios)} строк), "
      f"balance ({len(df_balance)} строк)")
